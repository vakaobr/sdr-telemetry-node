def country_for_hex(icao_hex: str) -> str | None:
    try:
        addr = int(icao_hex, 16)
    except ValueError:
        return None
    for lo, hi, name in _COUNTRY_RANGES:
        if lo <= addr <= hi:
            return name
    return None


def operator_for_callsign(callsign: str | None) -> str | None:
    if not callsign or len(callsign) < 3:
        return None
    return AIRLINES.get(callsign[:3].upper())


def type_name_for(code: str | None) -> str | None:
    if not code:
        return None
    return TYPE_NAMES.get(code.upper())
